package Aula_4;

public class Rgb {
	private int r, g, b;

	public Rgb() {
		int r = 0, g = 0, b = 0;
	}

	public int getR() {
		return r;
	}

	public int getG() {
		return g;
	}

	public int getB() {
		return b;
	}

	public void setR(int r) {
		this.r = r;
	}

	public void setG(int g) {
		this.g = g;
	}

	public void setB(int b) {
		this.b = b;
	}

	public static Rgb ConverteCmykRgb(Cmyk cmyk) {

		// formula para converter CMYK para RGB

		Rgb rgb = new Rgb();

		return rgb;

	}

}
